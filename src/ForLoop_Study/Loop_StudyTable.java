package ForLoop_Study;

public class Loop_StudyTable {

	public static void main(String[] args) {

		//print 6 table incement & decement
		
		int a;
		for(a=6;a<=60;a=a+6)
		{
			System.out.println("Table of 6 is Inc :"+a);
		}
		
		System.out.println("******************");
		int i;
		for(i=60;i>=6;i=i-6)
		{
			System.out.println("Table of 6 is in Dec :"+i);
		}
	}

}
